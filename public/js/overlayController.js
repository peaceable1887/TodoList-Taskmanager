/**
 * The function shows and hides the overlays
 *
 * @version 1.1
 */

/**
 * The function shows the sort overlay
 */
function showOverlaySortTask() {

    //Show the sort overlay
    document.getElementById("overlaySortContainer").style.display = "block";
    document.getElementById("overlaySort").style.display = "block";
}

/**
 * The function hides the sort overlay
 */
function hideOverlaySortTask() {

    //Hide the sort overlay
    document.getElementById("overlaySortContainer").style.display = "none";
    document.getElementById("overlaySort").style.display = "none";
}

